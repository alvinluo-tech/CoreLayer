import { readFileSync, writeFileSync, readdirSync, existsSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const root = join(__dirname, "..");
const sqlDir = join(root, "daemon", "drizzle");
const targetFile = join(root, "daemon", "src", "persistence", "migrations.ts");

if (!existsSync(sqlDir)) {
  console.error("Drizzle directory does not exist:", sqlDir);
  process.exit(1);
}

const sqlFiles = readdirSync(sqlDir)
  .filter(f => f.endsWith(".sql"))
  .sort();

const migrations = sqlFiles.map(file => {
  const id = file.replace(".sql", "");
  const sqlContent = readFileSync(join(sqlDir, file), "utf-8");
  return { id, sql: sqlContent };
});

const tsContent = `// Automatically generated by scripts/compile-migrations.mjs. Do not edit directly.
export interface Migration {
  id: string;
  sql: string;
}

export const migrations: Migration[] = ${JSON.stringify(migrations, null, 2)};
`;

mkdirSync(dirname(targetFile), { recursive: true });
writeFileSync(targetFile, tsContent, "utf-8");
console.log(`[Migrations] Compiled ${migrations.length} migrations into ${targetFile}`);
